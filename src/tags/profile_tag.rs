impl std::fmt::Display for ProfileTag {
    fn fmt(&self, f: &mut std::fmt::Formatter) -> std::fmt::Result {
        let value = self.to_u32();
        let bytes = value.to_be_bytes();
        let s = String::from_utf8_lossy(&bytes);
        if s.is_ascii() && s.len() == 4 {
            write!(f, "{}", s)
        } else {
            write!(f, "{:08X}", value)
        }
    }
}

